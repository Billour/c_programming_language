#include <stdio.h>
#include <stdlib.h>

int main(void)
{
    int i_number = 84;

    /* Output area*/
    printf("Number  %d  \n", i_number);
    printf("Decimal  %d  \n", i_number);
    printf("Octal  %o  \n", i_number);
    printf("Hexadecimal lowercase  %x  \n", i_number);
    printf("Hexadecimal uppercase  %X  \n", i_number);
    system("pause");
    return 0;
}
